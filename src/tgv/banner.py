# Gradient colors (top to bottom): purple #833AB4 ‚Üí red #FD1D1D ‚Üí orange #FCB045
GRADIENT = [
    "#833AB4",
    "#9C2E9E",
    "#B52388",
    "#D01A5E",
    "#E91D3A",
    "#F46A28",
    "#FCB045",
]


def print_banner() -> None:
    """Print the gradient-colored banner to terminal."""
    console = Console()
    console.print()
    for i, line in enumerate(BANNER_LINES):
        text = Text(line)
        text.stylize(GRADIENT[i])
        console.print(text, justify="center")
    # Subtitle with a soft color
    console.print()
    sub = Text(SUBTITLE)
    sub.stylize("italic #D01A5E")
    console.print(sub, justify="center")
    console.print()


